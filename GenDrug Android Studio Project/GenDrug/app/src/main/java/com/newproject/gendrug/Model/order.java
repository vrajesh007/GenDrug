package com.newproject.gendrug.Model;

public class order {

    String Order_id, Order_products, Order_quantity,Order_amount, Order_date;

    public String getOrder_id() { return Order_id; }
    public void setOrder_id(String Order_id) {
        this.Order_id = Order_id;
    }

    public String getOrder_products() {
        return Order_products;
    }
    public void setOrder_products(String Order_products) {
        this.Order_products = Order_products;
    }

    public String getOrder_quantity() {
        return Order_quantity;
    }
    public void setOrder_quantity(String Order_quantity) {
        this.Order_quantity = Order_quantity;
    }

    public String getOrder_amount() { return Order_amount; }
    public void setOrder_amount(String Order_amount) {
        this.Order_amount = Order_amount;
    }

    public String getOrder_date() {
        return Order_date;
    }
    public void setOrder_date(String Order_date) {
        this.Order_date = Order_date;
    }

}


